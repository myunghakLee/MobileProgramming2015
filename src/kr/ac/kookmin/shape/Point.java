package kr.ac.kookmin.shape;

public class Point {

	private int x; // in year
	private int y; // in year
	
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}	

	public void setX(int a) {
		x=a;
	}	
	public void setY(int a) {
		y=a;
	}	
	public Point(int x, int y)
	{
		this.x=x;
		this.y=y;
	}

}


